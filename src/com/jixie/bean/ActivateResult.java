package com.jixie.bean;

//注册验证结果
public class ActivateResult {

	private String activateMsg;//验证信息
	private int activateEffect;//验证结果,1=成功，2=链接失效，3=链接无效，4=跳转失败，需要重新验证
	public String getActivateMsg() {
		return activateMsg;
	}
	public void setActivateMsg(String activateMsg) {
		this.activateMsg = activateMsg;
	}
	public int getActivateEffect() {
		return activateEffect;
	}
	public void setActivateEffect(int activateEffect) {
		this.activateEffect = activateEffect;
	}
	
	
}
